package Model;

public class CadastroSaborPizzaModel {
    private String nomePizza;
    private String ingredientes;
    private String tamanhoPequeno;
    private String tamanhoMedio;
    private String tamanhoGrande;

    public String getNomePizza() {
        return nomePizza;
    }

    public void setNomePizza(String nomePizza) {
        this.nomePizza = nomePizza;
    }

    public String getIngredientes() {
        return ingredientes;
    }

    public void setIngredientes(String ingredientes) {
        this.ingredientes = ingredientes;
    }

    public String getTamanhoPequeno() {
        return tamanhoPequeno;
    }

    public void setTamanhoPequeno(String tamanhoPequeno) {
        this.tamanhoPequeno = tamanhoPequeno;
    }

    public String getTamanhoMedio() {
        return tamanhoMedio;
    }

    public void setTamanhoMedio(String tamanhoMedio) {
        this.tamanhoMedio = tamanhoMedio;
    }

    public String getTamanhoGrande() {
        return tamanhoGrande;
    }

    public void setTamanhoGrande(String tamanhoGrande) {
        this.tamanhoGrande = tamanhoGrande;
    }
}
